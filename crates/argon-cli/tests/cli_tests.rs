#[test]
fn test_compile_example_control_flow_uses_cfg_in_ir() {
    let temp_dir = tempfile::tempdir().unwrap();
    let example = PathBuf::from(env!("CARGO_MANIFEST_DIR"))
        .join("..")
        .join("..")
        .join("examples")
        .join("control-flow.arg");

    let mut cmd = cargo_bin_cmd!("argon");
    cmd.arg("compile")
        .arg(&example)
        .arg("--pipeline")
        .arg("ir")
        .arg("-o")
        .arg(temp_dir.path().join("out.js"))
        .assert()
        .success();

    let output = fs::read_to_string(temp_dir.path().join("out.js")).unwrap();
    // The IR CFG emitter uses a bb state machine.
    assert!(output.contains("switch (__bb)"));
    assert!(output.contains("console.log(i)"));
}

